package org.example.model;

public class BackGoods {
    private String companyId;
    private String companyName;
    private String companyNo;
    private String companyShort;
    private String companyShortEn;
    private String sku;
    private String skuName;
    private Long skuNum;
    private String createTime;
    private String backOrders;
    private String complete;

    public String getCompanyId() {
        return companyId;
    }
    public void setCompanyId(String companyId) {
        this.companyId = companyId;
    }
    public String getCompanyName() {
        return companyName;
    }
    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }
    public String getCompanyNo() {
        return companyNo;
    }
    public void setCompanyNo(String companyNo) {
        this.companyNo = companyNo;
    }
    public String getCompanyShort() {
        return companyShort;
    }
    public void setCompanyShort(String companyShort) {
        this.companyShort = companyShort;
    }
    public String getCompanyShortEn() {
        return companyShortEn;
    }
    public void setCompanyShortEn(String companyShortEn) {
        this.companyShortEn = companyShortEn;
    }
    public String getSku() {
        return sku;
    }
    public void setSku(String sku) {
        this.sku = sku;
    }
    public String getSkuName() {
        return skuName;
    }
    public void setSkuName(String skuName) {
        this.skuName = skuName;
    }
    public String getCreateTime() {
        return createTime;
    }
    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }
    public Long getSkuNum() {
        return skuNum;
    }
    public void setSkuNum(Long skuNum) {
        this.skuNum = skuNum;
    }
    public String getBackOrders() {
        return backOrders;
    }
    public void setBackOrders(String backOrders) {
        this.backOrders = backOrders;
    }
    public String getComplete() {
        return complete;
    }
    public void setComplete(String complete) {
        this.complete = complete;
    }
}
